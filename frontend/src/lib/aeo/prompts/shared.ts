/** 사용자 입력 주제가 있을 때만 합성. 없으면 빈 문자열 반환. */
export function buildTopicSection(topic?: string | null): string {
  if (!topic || topic.trim().length === 0) return "";
  return `[글의 주제 — 사용자 지정]
이 글의 주제는 다음과 같습니다:
${topic.trim()}

제목과 본문은 위 주제를 정확히 다루어야 합니다.`;
}

/** 타겟 자연어 질문 목록 (Phase 3에서 채워짐). 비어있으면 빈 문자열. */
export function buildTargetQueriesSection(queries?: string[] | null): string {
  if (!queries || queries.length === 0) return "";
  return `[타겟 자연어 질문 — 이 글이 답해야 할 질문들]
다음 질문들은 사용자가 AI(ChatGPT·Claude 등)에게 실제로 던질 법한 자연어 질문입니다.
이 글은 아래 질문 모두에 대한 답이 본문 어딘가에 들어 있어야 합니다 (FAQ 섹션 또는 본문 흐름 안에서 자연스럽게):

${queries.map((q, i) => `${i + 1}. ${q}`).join("\n")}`;
}

/** 사용자 출처/근거 목록. 비어있으면 빈 문자열. */
export function buildSourcesSection(
  sources?: Array<{ url?: string; note?: string }> | null
): string {
  if (!sources || sources.length === 0) return "";
  const lines = sources
    .map((s) => {
      const parts: string[] = [];
      if (s.url) parts.push(s.url);
      if (s.note) parts.push(s.note);
      return parts.length ? `- ${parts.join(" — ")}` : "";
    })
    .filter(Boolean);
  if (lines.length === 0) return "";
  return `[사용자 제공 출처·근거 — 본문에 인용]
다음 자료를 본문 어딘가에서 1회 이상 자연스럽게 인용하라 (예: "○○에 따르면", "${"\\${자료}"}을 보면").

${lines.join("\n")}`;
}

/** 글자수 범위 섹션 */
export function buildCharCountSection(charCount: { min: number; max: number }): string {
  return `[글자수 범위]
${charCount.min}자 ~ ${charCount.max}자 사이. 공백 포함.`;
}

