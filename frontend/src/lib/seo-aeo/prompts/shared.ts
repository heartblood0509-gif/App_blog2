export function buildSeoAeoSharedRules(): string {
  // 순서: 출력 메타 금지(최강) → 가독성 기본 → 가독성 추가 강화 → 이미지(필수) → 마커/표 금지 → 톤(소프트)
  // LLM이 "절대 위반 금지" 톤의 강한 규칙부터 받도록 배치
  return [
    BRAND_RESPONSE_FORMAT,
    BRAND_READABILITY_RULES_DEFAULT,
    SEO_AEO_READABILITY_EXTRA,
    SEO_AEO_IMAGE_RULES,
    SEO_AEO_HEADING_MARKER_RULES,
    SEO_AEO_TABLE_BAN_RULES,
    SEO_AEO_FIXED_TONE,
  ].join("\n\n");
}
