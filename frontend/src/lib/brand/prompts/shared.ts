/** 사용자 입력 주제가 있을 때만 합성. 없으면 빈 문자열 반환. */
export function buildTopicSection(topic?: string | null): string {
  if (!topic || topic.trim().length === 0) return "";
  return `[글의 주제 — 사용자 지정]
이 글의 주제는 다음과 같습니다:
${topic.trim()}

제목과 본문은 위 주제를 정확히 다루어야 합니다. 키워드는 도구일 뿐, 글의 중심은 이 주제입니다.`;
}

/** 모든 공통 규칙을 한 번에 합쳐 반환. 템플릿 프롬프트 끝부분에 붙임. */
export function buildSharedRules(): string {
  return [
    BRAND_RESPONSE_FORMAT,
    BRAND_SEO_RULES,
    BRAND_READABILITY_RULES,
    BRAND_TITLE_RULES,
    BRAND_AVOID_AD_TONE,
  ].join("\n\n");
}
