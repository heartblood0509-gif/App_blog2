/**
 * 본문에 등장하는 "절대 금지 단어"(BANNED_*)를 문맥에 맞는 자연스러운
 * 한국어 1단어로 대체하기 위한 프롬프트.
 *
 * 응답은 반드시 `{ "원단어": "대체어", ... }` 형식의 JSON 1개로만.
 * 라우트가 responseMimeType: "application/json"을 강제하므로
 * 추가 텍스트 없이 객체 하나가 정답.
 */
export function buildReplaceForbiddenPrompt(
  content: string,
  words: string[],
): string {
  const wordList = words.map((w) => `- ${w}`).join("\n");

  return `너는 한국어 블로그 글의 표현을 자연스럽게 다듬는 편집자다.

다음 본문에 들어있는 "금지 단어"들을, 그 단어가 등장하는 **문맥에 맞는 자연스러운 한국어 1단어**로 대체할 대체어를 찾아라.

# 규칙
1. 각 단어마다 **명사형 1단어**만 제안한다. 구절(2단어 이상)·문장 금지.
2. 원문 어조와 의미를 최대한 유지한다. 의미가 크게 달라지는 단어는 피한다.
3. 같은 단어가 본문에 여러 번 나와도 **모두 동일한 대체어 1개**로 처리한다.
4. 다음 단어들은 절대 사용하지 마라 (또 다른 금지어이기 때문):
   - 키워드, 무료, 최저가, 도박, 카지노, 토토, 슬롯, 배팅, 베팅, 치료, 완치, 처방, 폭탄, 살인, 마약
5. **반드시 JSON 객체 1개**만 출력한다. 그 외의 문장·설명·코드블록·마크다운 금지.

# 출력 형식
{
  "금지단어1": "대체어1",
  "금지단어2": "대체어2"
}

# 금지 단어 목록
${wordList}

# 본문
${content}
`;
}
