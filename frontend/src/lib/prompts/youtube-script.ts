/**
 * 블로그 본문 → 유튜브 스크립트 변환을 위한 "매칭 찾기" 프롬프트.
 *
 * 설계 원칙 (사용자와 합의된 D안):
 * - AI는 본문을 절대 다시 출력하지 않는다 (환각 위험 차단).
 * - AI는 "이 표현을 이 표현으로 바꿔야 한다"는 매칭 리스트만 JSON으로 반환.
 * - 실제 본문 치환은 코드(`applyMatches`)가 String.replaceAll로 수행한다.
 *
 * 이렇게 하면 본문 100% 보존이 구조적으로 보장된다.
 *
 * 호출처: `frontend/src/app/api/convert-youtube/route.ts`
 * 후속 처리: `frontend/src/lib/youtube-script/apply-matches.ts`
 */

export function buildYoutubeMatchPrompt(blogContent: string): string {
  return `당신은 텍스트 분석기입니다. 본문을 절대 출력하지 마라.

[작업]
다음 블로그 본문에서 "글 매체 → 영상 매체"로 표현을 바꿔야 할 부분만
찾아서 JSON 매칭 리스트로 반환하세요.

[찾을 패턴 카테고리]
- 동사: 읽다·읽으셨다·읽어보세요 류 → 보다·시청하다·보셨다·봐주세요 등
- 명사: 글·포스팅·블로그·포스트 → 영상
- 호칭: 독자·방문자 → 시청자
- 지시어: "이 글", "이 포스트", "이번 글" → "이 영상", "이번 영상"
- 마무리 인사: "끝까지 읽어주셔서" → "끝까지 시청해주셔서"
- 그 외 글 매체 전용 표현 (당신 판단으로 자연스러운 영상 매체 표현으로)

[절대 규칙 — 위반 시 결과 폐기]
1. 본문을 출력하지 마라. 매칭 리스트만 반환.
2. old 필드는 본문에 등장하는 "정확한 표현 그대로" (띄어쓰기·어미·조사 포함).
3. 단일 한 글자 매칭 금지. 예: "글" 한 글자만 매칭하면 "글로벌"·"한글" 같은 다른 단어가 오염됨.
4. 표현 단위로만 매칭하라.
   ✅ 좋음: "이 글을", "글을 읽으셨다면", "다음 포스팅"
   ❌ 나쁨: "글" (단독), "읽" (단독)
5. 같은 패턴이 여러 번 등장해도 한 번만 등록 (코드가 일괄 치환).
6. 매체 표현이 아닌 일반 단어는 건드리지 마라. 글의 내용·정보는 모두 보존.
7. 변환할 표현이 없다면 빈 리스트 반환.

[출력 형식 — JSON만, 다른 설명·코드블록 마커 X]
{ "matches": [{"old": "원문 그대로", "new": "치환 후 문구"}, ...] }

[입력 본문]
${blogContent}`;
}
