/**
 * 레퍼런스 블로그 글 분석 프롬프트
 */

export function buildAnalysisPrompt(referenceText: string): string {
  return `# 역할
너는 블로그 글 구조 분석 전문가야.
아래 레퍼런스 글을 상세히 분석해서 구조화된 분석 결과를 만들어줘.

## 분석할 레퍼런스 글
${referenceText}

## 분석 항목 (모두 포함해서 분석해줘)

### 1. 서사 구조 분석
- 글의 흐름을 8~15단계로 분해
- 각 단계별: 단계명, 해당 내용 요약, 감정 톤
- 서사 유형 분류:
  - 감정 선공형: 스트레스/공감으로 시작
  - 결론 선공형: 결과 먼저 보여주기
  - 관리형: 루틴/방법 소개
  - 정보 가이드형: 개념 → 비교 → 활용

### 2. 소제목 분석
- 소제목 개수, 스타일 (인용구/볼드/일반)
- 소제목이 호기심을 유발하는 방식
- 소제목 위치 (글 전체 대비 비율)

### 3. 톤 & 스타일 분석
- 구어체/문어체 비율 (예: "99% 구어체")
- 주요 문장 끝 패턴 (예: "~거든요", "~더라고요", "~했음")
- 평균 문장 길이
- 감정 표현 방법
- 줄바꿈 빈도

### 4. 제품 배치 분석
- 제품이 처음 언급되는 위치 (전체 글 대비 %)
- 언급 횟수
- 언급 톤 (직접적/간접적/우회적)
- 브랜드명 노출 여부

### 5. SEO 기본 정보
- 글자수 (공백 포함/제외)
- 키워드 반복 패턴
- 해시태그 개수

## 출력 형식
위 항목을 마크다운 형식으로 구조화해서 출력해.`;
}
