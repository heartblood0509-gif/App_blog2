/**
 * 레퍼런스 블로그 글 분석 프롬프트
 */

export function buildAnalysisPrompt(referenceText: string): string {
  return `# 역할
너는 블로그 글 구조 분석 전문가야.
아래 레퍼런스 글을 상세히 분석해서 구조화된 분석 결과를 만들어줘.

## 분석할 레퍼런스 글
${referenceText}

## 분석 항목 (모두 포함해서 분석해줘)

### 1. 서사 구조 분석
- 글의 흐름을 8~15단계로 분해
- 각 단계별: 단계명, 해당 내용 요약, 감정 톤
- 서사 유형 분류:
  - 감정 선공형: 스트레스/공감으로 시작
  - 결론 선공형: 결과 먼저 보여주기
  - 관리형: 루틴/방법 소개
  - 정보 가이드형: 개념 → 비교 → 활용

### 2. 소제목 분석
- 소제목 개수, 스타일 (인용구/볼드/일반)
- 소제목이 호기심을 유발하는 방식
- 소제목 위치 (글 전체 대비 비율)

### 3. 톤 & 스타일 분석
- 구어체/문어체 비율 (예: "99% 구어체")
- 주요 문장 끝 패턴 (예: "~거든요", "~더라고요", "~했음")
- 평균 문장 길이
- 감정 표현 방법
- 줄바꿈 빈도

### 4. 제품 배치 분석
- 제품이 처음 언급되는 위치 (전체 글 대비 %)
- 언급 횟수
- 언급 톤 (직접적/간접적/우회적)
- 브랜드명 노출 여부

### 5. SEO 기본 정보
- 글자수 (공백 포함/제외)
- 키워드 반복 패턴
- 해시태그 개수

## 출력 형식
위 항목을 마크다운 형식으로 구조화해서 출력해.

## 추가 출력 (마지막에 반드시 포함)
분석 결과 마지막에 아래 형식의 HTML 코멘트 **두 줄**을 차례로 추가해.
이 코멘트는 사용자에겐 안 보이고, 시스템이 시각화·톤 흉내에 사용해.

### 1) FLOW 코멘트 (단계 시각화용)
\`\`\`
<!-- FLOW: ["스트레스","문제 인식","악화","시도","실패","깨달음","기준 변화","제품 발견","변화","루틴","마무리"] -->
\`\`\`

규칙:
- 위 1번 "서사 구조 분석"에서 분해한 단계들을 **순서대로** 배열에 넣어
- 각 단계 이름은 **한국어 2~4글자**로 짧게 (카드에 짧게 표시되어야 함)
- 큰따옴표 사용한 유효한 JSON 배열, 한 줄

### 2) EXCERPTS 코멘트 (톤 본보기 — "레퍼런스 그대로" 모드 핵심)
\`\`\`
<!-- EXCERPTS: ["문장1","문장2","문장3","문장4","문장5","문장6","문장7","문장8"] -->
\`\`\`

규칙 (매우 중요 — 글 톤 흉내의 핵심):
- 원본 글에서 **어미·말투가 가장 잘 드러난 짧은 문장 8개**를 **그대로** 인용
- 선정 기준:
  - 한 문장 **20자 이내** 위주 (짧은 줄이 톤을 더 잘 보여줌)
  - **다양한 어미 패턴 포함** (느낌표·평서문·의문문 섞어서)
  - 도입부·중간·마무리에서 **골고루** 발췌
  - 평범한 정보 전달 문장보다 **감정·일상 표현 우선**
- 원본 그대로 복사 (수정·재구성 금지)
- 큰따옴표 사용한 유효한 JSON 배열, 한 줄

### 출력 순서
분석 결과 본문(마크다운) 다음에 빈 줄, FLOW 코멘트 한 줄, EXCERPTS 코멘트 한 줄 순서로 끝내.`;
}
