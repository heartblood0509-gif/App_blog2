export function buildThreadsImageGenerationPrompt(
  threadsContent: string,
  imageAnalysis?: string,
  style: ImageStyle = "realistic",
  imageIndex: number = 0
): string {
  const stylePrompt = IMAGE_STYLE_PROMPTS[style];
  const variationHint =
    imageIndex > 0
      ? "\n⚠️ 이전 이미지와 다른 구도, 다른 장면, 다른 앵글로 생성하세요. 동일한 이미지를 반복하지 마세요."
      : "";

  return `당신은 소셜 미디어 이미지 전문 디자이너입니다.
아래 스레드(Threads) 게시물 내용에 어울리는 이미지를 1장 생성해주세요.

## 스레드 게시물 내용
${threadsContent}

${
  imageAnalysis
    ? `## 레퍼런스 이미지 스타일 분석
${imageAnalysis}

위 레퍼런스의 시각적 스타일(밝기, 구도, 색감, 분위기)을 참고하되,
아래 지정된 이미지 스타일을 우선 적용하세요.`
    : ""
}

${stylePrompt}

## 필수 규칙
- 이미지 안에 텍스트, 글자, 워터마크를 절대 넣지 마세요
- 게시물의 핵심 메시지를 시각적으로 표현하세요
- 정확히 1장의 완성된 고품질 이미지만 생성하세요${variationHint}`;
}
